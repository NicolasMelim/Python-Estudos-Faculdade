entrada = input('Voce deseja entrar no sistema? Então digite "Quiver" para sim e "Not" para não: ')
if entrada == "Quiver":
    print('Voce entrou no nosso sistema, seja bem vindo! ')
elif entrada == "Not":
    print('Que pena ver uma pessoa desistindo de acessar o nosso sistem ): ')
else:
    print('Digite algumas das opções em aspas duplas! ')